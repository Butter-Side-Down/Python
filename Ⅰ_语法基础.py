# 六、字符串
def string():
    print("六、字符串========================================")
    # 1.字符串的简单操作
    #   🞉 字符串拼接：+
    #   🞉 字符串重复：*
    #   🞉 成员运算符：in  not in    可以判断字符串中是否(不)包含指定内容

    # 2.索引(下标)：[n]    正序从0开始，倒序从-1开始
    # 3.切片：[start:stop:step]
    #   🞉 参数详解：
    #     · start(可省略)：起始索引(包含，默认从0开始)
    #     · stop(可省略)：结束索引(不包含，默认到末尾)
    #     · step(正序时可省略)：步长(默认为1，负数表示从右往左)，倒序时必须指定步长
    #    注意：如果参数全部省略，至少要写为 [:]

    # 4.格式化字符串(拼接变量和文字)：
    #   🞉 %
    #     · 字符串中用 %类型 占位，字符串后用 %(参数1,参数2...[只有一个参数时括号可省略]) 一一对应
    #     · 格式化控制：% 条件 类型
    #    例：print("1+1的结果是%d"%2)
    #   🞉 str.format()
    #     · 字符串中用 {} 占位，字符串后用 .format(参数1,参数2...) 一一对应
    #     · 格式化控制：{:条件}
    #    例：print("1+1的结果是{}".format(2))
    # ※🞉 f-strings    Python3.6+推荐使用这个较新方式
    #     · 字符串前加上 f , 字符串中直接用 {参数} 即可
    #     · 格式化控制：{参数:条件}
    #    例：print(f"1+1的结果是{1+1}")
    #
    #   常见格式化符号(不冲突的可以叠加使用)：
    #    符号  适用体系 含义
    #   d      %和:    十进制整数
    #   f      %和:    浮点数
    #   s      %和:    字符串
    #   %      %和:    百分比
    #   e      %和:    科学计数法
    #   b      :       二进制
    #   o      %和:    八进制
    #   x/X    %和:    十六进制
    #   .n     %和:    保留n位(位数或长度)
    #   *n     :       补位字符*和宽度n
    #   ,      :       千位分隔符
    #   >      :       右对齐(数值默认右对齐)
    #   <      :       左对齐(字符串默认左对齐)
    #   ^      :       居中对齐

    # 5.字符串常用内建函数(注意这些函数不会修改原字符串而是会创建新字符串)
    #   🞉 查找类函数(找字符/统计次数)
    #     · .find(str,start,end)        查找子串首次出现的位置，没找到则返回-1
    #     · .index(str,start,end)       查找子串首次出现的位置，没找到则报错
    #     · .count(str,start,end)       统计子串出现的次数，没找到则返回0
    #   🞉 修改类函数(改内容/分割/去空格)
    #     · .replace(old,new,count)     替换字串，count指定替换的次数，默认为全部替换
    #     · .split(sep,num)             分割字符串，num指定分割的次数，默认为全部分割
    #     · .strip(chars)               去除字符串首尾指定的字符(默认空格或换行符)
    #     · .upper()                    英文字母全转大写
    #     · .lower()                    英文字母全转小写
    #   🞉 判断类函数(判断字符串属性)
    #     · .startswith(prefix)         判断是否以指定前缀开头
    #     · .endswith(suffix)           判断是否以指定后缀结尾
    #     · .islower()                  判断其中英文字母是否全为小写
    #     · .isupper()                  判断其中英文字母是否全为大写
    #   🞉 编码解码(处理字符串/字节)
    #     · .encode(encoding,errors)    将字符串编码为字节，指定编码方式，指定错误处理方式
    #     · .decode(encoding,errors)    将字节解码为字符串，指定编码方式，指定错误处理方式

    # 例：
    a = "Python is an extremely convenient language"
    print("%s"%(a[4:31:2])+'\n'+(a[:6])+'\n'+(a[::-1]))
    b = "extreme"
    print("字符串a中包含%s吗？%s"%(b,(b in a)))
    print("1+1的结果是{}".format(2))
    print(f"1+1的结果是{1 + 1}")
    c = b.split("e",1)
    print(c)
    print("ABCD一二三四1234".isupper())

    print()
