def withdraw(amount,account_balance,account_is_frozen):
    if account_is_frozen:
        raise Exception("账户已冻结")
    if amount > account_balance:
        raise Exception("余额不足")
    account_balance -= amount
    print("取款成功，取款金额：",amount)
